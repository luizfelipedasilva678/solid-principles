import {
  IndividualCustomer,
  EnterpriseCustomer,
} from './interfaces/customer-protocol';

export class Individual implements IndividualCustomer {
  firstName: string;
  lastName: string;
  cpf: string;

  constructor(firstName: string, lastName: string, cpf: string) {
    this.firstName = firstName;
    this.lastName = lastName;
    this.cpf = cpf;
  }
}

export class Enterprise implements EnterpriseCustomer {
  name: string;
  cnpj: string;

  constructor(name: string, cnpj: string) {
    this.name = name;
    this.cnpj = cnpj;
  }
}
